DECLARE
	i int;
BEGIN
	FOR i IN 1..200 LOOP
		UPDATE order_line
		SET order_line_total = (SELECT SUM(product_price * line_quantity)
		FROM order_line
		JOIN line
		ON order_line.line_id = line.line_id
		JOIN product
		ON line.product_id = product.product_id
		WHERE order_line_id = i)
		WHERE order_line_id = i;
	END LOOP;
END
$do$;

